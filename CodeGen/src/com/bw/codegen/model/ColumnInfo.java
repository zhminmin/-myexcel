package com.bw.codegen.model;

/**
 * 数据库表列信息
 * @author
 */
public class ColumnInfo
{
	/** 是否主键 pri */
	private String columnKey;
	/** 字段名称 */
	private String columnName;
	/** 字段类型 */
	private String columnType;
	/** 列描述 */
	private String columnComment;
	/** 字段长度 */
	private String columnLength;

	/** Java属性类型 */
	private String attrType;
	/** Java属性名称(第一个字母大写)，如：user_name => UserName */
	private String attrName;
	/** Java属性名称(第一个字母小写)，如：user_name => userName */
	private String attrname;

	public String getColumnName()
	{
		return columnName;
	}

	public void setColumnName(String columnName)
	{
		this.columnName = columnName;
	}

	public String getColumnType()
	{
		return columnType;
	}

	public void setColumnType(String columnType)
	{
		this.columnType = columnType;
	}

	public String getColumnComment()
	{
		return columnComment;
	}

	public void setColumnComment(String columnComment)
	{
		this.columnComment = columnComment;
	}

	public String getAttrName()
	{
		return attrName;
	}

	public void setAttrName(String attrName)
	{
		this.attrName = attrName;
	}

	public String getAttrname()
	{
		return attrname;
	}

	public void setAttrname(String attrname)
	{
		this.attrname = attrname;
	}

	public String getAttrType()
	{
		return attrType;
	}

	public void setAttrType(String attrType)
	{
		this.attrType = attrType;
	}

	@Override
	public String toString()
	{
		return "Gen [columnName=" + columnName + ", columnType=" + columnType + ", columnLength=" + columnLength + ", columnComment=" + columnComment + ", attrName=" + attrName + ", attrname=" + attrname + ", attrType=" + attrType + ", columnKey=" + columnKey + "]";
	}

	public String getColumnKey()
	{
		return columnKey;
	}

	public void setColumnKey(String columnKey)
	{
		this.columnKey = columnKey;
	}

	public String getColumnLength()
	{
		return columnLength;
	}

	public void setColumnLength(String columnLength)
	{
		this.columnLength = columnLength;
	}
}